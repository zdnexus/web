'use strict';

// JavaScript 是一门 动态的、弱类型的、面向对象的、解释型的 编程语言，非常适合面向对象和函数式的编程风格。

// ECMAScript 标准由语法、数据类型、语句、关键字、保留字、运算符、对象组成。
// 它与 Web 浏览器没有任何依赖关系，并且这门语言本身并不包含输入和输出定义。
// ECMAScript 定义的只是这门语言的基础，而在此基础之上可以构建更完善的脚本语言。

// 文档对象模型（DOM，Document Object Model）是用于 HTML 的应用程序编程接口（API），它把整个页面映射为一个多层节点结构。
// HTML页面中的每个组成部分都是某种类型的节点，这些节点又包含着不同类型的数据。

// 浏览器对象模型（BOM，Browser Object Model）是用于浏览器的应用程序编程接口（API），它把整个浏览器窗口映射为一个对象。
// 从根本上讲，BOM 只处理浏览器窗口和框架，但人们习惯上也把所有针对浏览器的JavaScript 扩展算作 BOM 的一部分。

// JavaScript 和 ECMAScript 通常被人们用来表达相同的含义，但 JavaScript 的含义却比 ECMAScript 中规定的要多得多。
// 一个完整的 JavaScript 实现应该由下面三个不同的部分组成。
// 核心（ECMAScript），由 ECMA-262 定义，提供核心语言功能。
// 文档对象模型（DOM），提供访问和操作网页内容的方法和接口。
// 浏览器对象模型（BOM），提供与浏览器交互的方法和接口。